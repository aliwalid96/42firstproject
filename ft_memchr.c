void *memchr(const void *s, int c, size_t n)
{
	int i;
	i=0;
	unsigned char * s2;

	s2=s;

	while(i<n)
	{
		if(*s2==c)
			return(void *) s2;
		i++;
		s2++;
	}
	return(NULL);

}
